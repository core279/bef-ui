  // 언어코드에 변경
  public static set(lang: string) {

    let message: Object = null;

    // 한국어
    if (lang == this.KOREAN) {
      message = this.messageKorean;
    }

    // 영어
    else if (lang == this.ENGLISH) {
      message = this.messageEnglish;
    }

    else {
      lang = this.ENGLISH;
      message = this.messageEnglish;

    }

    // 선택한 언어코드 변경
    Message.currentLanguage = lang;
    // 선택한 언어코드에 해당하는 메시지 변경
    Message.currentMessage = message;
  }

  // key 해당하는 message 반환
  public static get(key: string) {
    return Message.currentMessage[key];
  }

